public class Person {
    public String name;
    void setName (String name){
        this.name = name;
    }
    public int idade;

    public void setIdade(int idade) {
        this.idade = idade;
    }

    public String sexo;

    public void setSexo(String sexo) {
        this.sexo = sexo;
    }

    String getName(){
        return name;
    }

    int getIdade(){
        return idade;
    }
    String getSexo(){
        return sexo;
    }
}






